const bank = document.querySelector("#bank")
let guess = 1

addFighterToBank()

const rankBoxes = document.querySelectorAll('.rank-box')

let dragging
let placed = []
let allowed = false

rankBoxes.forEach(rb => {
    rb.addEventListener('dragover', dragOver)
    rb.addEventListener('dragleave', dragLeave)
    rb.addEventListener('drop', dropped)
})

function dragStart(e) {
    dragging = e.target
}

function dropped(e) {
  let id = e.target.id.slice(-1)
    if (e.target.className == "rank-box" && !placed.includes(id)) {
        e.target.append(dragging)
        placed.push(id)
        dragging.setAttribute("draggable", "false")
        guess++
        if (guess < 6) {addFighterToBank()}
        let num = document.querySelector("#r" + id)
        num.style['color'] = "gold";
    }
}

function dragOver(e) {
    e.preventDefault()
    let id = e.target.id.slice(-1)
    let num = document.querySelector("#r" + id)
    if (num.style['color'] != "gold") {num.style['color'] = "lightgray";}
}

function dragLeave(e) {
    let id = e.target.id.slice(-1)
    let num = document.querySelector("#r" + id)
    if (num.style['color'] != "gold") {num.style['color'] = "black";}
}

function addFighterToBank() {
    let fighter = fighters[Math.floor(Math.random() * 247)]

    console.log(fighter)

    let box = document.createElement('div')
    box.setAttribute('class', 'fighter-box')
    box.setAttribute('draggable', 'true')
    
    let img = document.createElement('img')
    img.setAttribute("class", "fighter-img")
    img.setAttribute("src", fighter.url)
    img.setAttribute('draggable', 'false')

    let nameDiv = document.createElement('div')
    nameDiv.style['height'] = '100%'
    nameDiv.style['display'] = 'flex'
    nameDiv.style['flex-direction'] = 'column'
    nameDiv.style['justify-content'] = 'space-around'

    let name = document.createElement('h4')
    name.setAttribute('class', 'fighter-name')
    name.innerText = fighter.name + " (" + guess + ")"
    
    nameDiv.appendChild(name)

    box.appendChild(img)
    box.appendChild(nameDiv)
    box.addEventListener('dragstart', dragStart)
    bank.append(box)
}